import UIKit

extension UIFont {
    static func futura24() -> UIFont? {
        UIFont.init(name: "Futura", size: 24)
    }
    static func futura20() -> UIFont? {
        UIFont.init(name: "Futura", size: 20)
    }
    static func futura22() -> UIFont? {
        UIFont.init(name: "Futura", size: 22)
    }
    
    static func futura16() -> UIFont? {
        UIFont.init(name: "Futura", size: 16)
    }
}
